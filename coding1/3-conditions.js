//with coersion

if('133' == '133'){
    console.log("'133' == '133'")
}

if('133' != '132'){
    console.log("'133' != '132'")
}

if(12345 == 12345){
    console.log('12345 == 12345')
}

if(12345 == '12345'){
    console.log('12345 == \"12345\"')
}

if(0 == ''){
    console.log("0 == ''")
}

//without coersion

if('133' === '133'){
    console.log("'133' === '133'")
}

if('133' !== '132'){
    console.log("'133' !== '132'")
}

if(12345 === 12345){
    console.log('12345 === 12345')
}

if(12345 !== '12345'){
    console.log("12345 !== '12345'")
}

if(0 !== ''){
    console.log("0 !== ''")
}


